const findKeyByValue = function(object, value) {
  const result = [];
  for (let key in object) {
    if (object[key] === value) {
      return value;
    } else {
      return undefined
    }
  }
  return result;
};

const bestTVShowsByGenre = { 
  sci_fi: "The Expanse",
  comedy: "Brooklyn Nine-Nine",
  drama:  "The Wire"
};

assertEqual(findKeyByValue(bestTVShowsByGenre, "The Wire"), "drama");
